package br.com.jawc.collection.tarefaCollection2;

public class Pessoa {
    private String nome;
    private String sexo;

    public Pessoa(String nome, String sexo) {
        this.nome = nome;
        this.sexo = sexo;
    }
    public String getNome() {
        return nome;
    }

    public String getSexo() {
        return sexo;
    }

}
